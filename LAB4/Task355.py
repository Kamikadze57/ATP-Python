# Вводиться додатне дійсне число, надрукуйте 
# його першу цифру справа від десяткової точки.

num = input("Введіть дійсне число: ")
if float(num) <= 0: 
    print("Число від'ємне або дорівнює нулю.")
else:
    if '.' in str(num):
        dot = str(num).find('.')
        if len(str(num)) > dot + 1:
            print(f"Перша цифра після крапки: {str(num)[dot + 1]}")
    else:
        print("У числі немає дробової частини.")