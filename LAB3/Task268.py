# За даним натуральним числом n (n ≤ 9) виведіть драбинку з n сходинок 
# як у вихідних даних, n-а сходинка складається з чисел від 1 до n без пропусків.

number = int(input("Введіть натуральне число (≤ 9): "))
for i in range(1, number + 1):
    for j in range(1, i + 1):        
        print(j, end="")
    print()