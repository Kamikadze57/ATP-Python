# Надрукувати всі цілі числа від a до b включно, кратні деякому числу c.
# Числа a, b, c - цілі числа, які вводить користувач.

startNum = int(input("Введіть початкове ціле число: "))
endNum = int(input("Введіть кінцеве ціле число: "))
step = int(input("Введіть ціле число для кратності: "))

for i in range(startNum, endNum, step):
    print(i)
