# Є монотонна послідовність, в якій кожне натуральне число k
# зустрічається рівно k разів: 1, 2, 2, 3, 3, 3, 4, 4, 4, 4, .... За введеним
# натуральним числом n виведіть перші n членів цієї послідовності.

number = int(input("Введіть натуральне число: "))        
count = 0
for i in range(1, number + 1):
    for j in range(i):
        count += 1
        if count <= number:
            print(i, end=" ")