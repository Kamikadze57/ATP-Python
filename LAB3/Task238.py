# Надрукувати таблицю значень 1, 2, … n доларів США у переведенні на гривні
# за поточним курсом k (значення курсу вводиться з клавіатури).

amount = int(input("Введіть кількість доларів: "))
course = float(input("Введіть курс долара до гривні: "))

for i in range(1, amount + 1):
    dolarInUah = round(i * course, 2)
    print(f"{i} долара/доларів = {dolarInUah} гривень")